    protected void CargarEntidadAlEditar(EntidadAbstracta entidad) {
        familiaSelected = (FamiliaEquipo) entidad;
        txtNombre.setText(familiaSelected.getNombre());      
    }

    @Override
    protected boolean GuardarEntidadAlEditar() {
        familiaSelected.setNombre(txtNombre.getText());
        return familiaSelected.validate();
    }

