    @Action
    public Task agregarEquipo() {
        if(nInternoText.getText().isEmpty()){
            OpcionPanel.showMessageDialog(null, "El campo N° INTERNO es obligatorio.", "Error!", JOptionPane.ERROR_MESSAGE);
            return null;
        }else{
            return new AgregarEquipoTask(org.jdesktop.application.Application.getInstance(zilleprojects.ZilleProjectsApp.class));
        }
    }

    private class AgregarEquipoTask extends org.jdesktop.application.Task<Object, Void> {
        Equipos a= new Equipos();
        int id=0;
        AgregarEquipoTask(org.jdesktop.application.Application app) {
            super(app);
            a.setN_interno(nInternoText.getText());
            a.setMarca(marcaText.getText());
            a.setEquipos(equiposText.getText());
            a.setModelo(modeloText.getText());
            a.setDominio(dominioText.getText());
            a.setAño(añoText.getYear());
            if(vto_vt.getDate()!=null){
                a.setVto_vtv(vto_vt.getDate());
            }
            if(vto_seguro_nuevo.getDate()!=null){
                a.setVto_seguro(vto_seguro_nuevo.getDate());
            }
            if(vto_otros1.getDate()!=null){
                a.setVto_otros1(vto_otros1.getDate());
            }
            if(vto_otros2.getDate()!=null){
                a.setVto_otros2(vto_otros2.getDate());
            }
            if(vto_otros2.getDate()!=null){
                a.setVto_otros3(vto_otros2.getDate());
            }
            a.setDescripcion_vto1(descripcionVto1.getText());
            a.setDescripcion_vto2(descripcionVto2.getText());
            a.setDescripcion_vto3(descripcionVto3.getText());
        }
        @Override protected Object doInBackground() {
            EquiposDAO aDao= new EquiposDAO();
            aDao.conectar();
            id=aDao.guardar(a);
            return null;  // return your result
        }
        @Override protected void succeeded(Object result) {
            if (id>0){
                JOptionPane.showMessageDialog(null, "Equipamiento ingresado al sistema.",
                        "Equipos", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }   else{
                JOptionPane.showMessageDialog(null, "Error! Intente nuevamente.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

