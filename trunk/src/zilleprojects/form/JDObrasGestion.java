    public void mostrarDatos(Obras o) {
        idText.setText(String.valueOf(o.getId()));
        codigoText.setText(o.getCodigo());
        obraText.setText(o.getObra());
        contratoText.setText(o.getContrato());
        cuitText.setText(o.getCuit());
        comitenteText.setText(o.getComitente());
        plazoText.setText(o.getPlazo());
        lugarText.setText(o.getLugar());
        if (o.getFecha_inicio() != null) {
            fechaInicio.setDate(o.getFecha_inicio());
        } else {
            fechaInicio.setDate(null);
        }
        responsableText.setText(o.getResponsable());
        alertasObras.setText(null);
        
    }
    
