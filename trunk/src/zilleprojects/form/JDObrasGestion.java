    public void recargarInfo() {
        ObrasDAO odao = new ObrasDAO();
        dcbm = new DefaultComboBoxModel();
        odao.conectar();
        ArrayList<Obras> o = odao.cargarTodos();
        for (int i = 0; i < o.size(); i++) {
            dcbm.addElement((Obras) o.get(i));
        }
        comboObras.setModel(dcbm);
    }

